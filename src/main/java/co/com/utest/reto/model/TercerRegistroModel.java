package co.com.utest.reto.model;

public class TercerRegistroModel {

    private String yourComputer;
    private String version;
    private String language;
    private String yourMobileDevice;
    private String model;
    private String operatingSystem;

    public String getYourComputer() {
        return yourComputer;
    }

    public void setYourComputer(String yourComputer) {
        this.yourComputer = yourComputer;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getYourMobileDevice() {
        return yourMobileDevice;
    }

    public void setYourMobileDevice(String yourMobileDevice) {
        this.yourMobileDevice = yourMobileDevice;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getOperatingSystem() {
        return operatingSystem;
    }

    public void setOperatingSystem(String operatingSystem) {
        this.operatingSystem = operatingSystem;
    }
}
